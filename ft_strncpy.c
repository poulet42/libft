#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t n)
{
	size_t i;

	i = 0;
	while (*src != '\0' && n > 0 && n--)
		dst[i++] = *src++;
	while (n > 0 && n--)
		dst[i++] = '\0';
	return (dst);
}

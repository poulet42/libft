#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t n)
{
	char *s2;

	s2 = dst;
	while (*src != '\0' && n > 0 && n--)
		*s2++ = *src++;
	while (n > 0 && n--)
		*s2++ = '\0';
	return (s2);
}

#include "libft.h"

int	ft_count_nbr(int nbr)
{
	int temp;
	int i;

	i = 0;
	if (nbr == 0)
		return (1);
	while (nbr != 0)
	{
		nbr = nbr / 10;
		i++;
	}
	return (i);
}
